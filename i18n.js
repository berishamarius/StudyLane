/* ── setLanguage (used by renderer.js) ──────────────────────────── */
let currentLang = 'de';

function t(key) {
  const lang = TRANSLATIONS[currentLang] || TRANSLATIONS['de'];
  return lang[key] || TRANSLATIONS['de'][key] || key;
}

function setLanguage(code) {
  const lang = LANGUAGES.find(l => l.code === code);
  if (!lang) return;
  currentLang = code;
  document.documentElement.lang = code;
  document.documentElement.dir = lang.dir;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });
  const flagEl = document.getElementById('langFlag');
  const labelEl = document.getElementById('langLabel');
  if (flagEl) flagEl.textContent = lang.flag;
  if (labelEl) labelEl.textContent = lang.code.toUpperCase();
}
